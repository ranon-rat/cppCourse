#include <iostream>
using namespace std;
int main(){
    cout << "the size of an char is : " << sizeof(char)*8 << endl;
    cout << "the size of an short int is : " << sizeof(short int)*8 << endl;
    cout << "the size of an int  is : " << sizeof(int)*8 << endl;
    cout << "the size of an long int is : " << sizeof(long int)*8 << endl;   
    cout << "the size of an long long int is : " << sizeof(long long int)*8 << endl; 
    return 0;
}